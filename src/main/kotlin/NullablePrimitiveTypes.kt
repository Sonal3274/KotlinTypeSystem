fun main(args: Array<String>) {
    var nullableInt: Int? = null
    var nullableDouble: Double? = null
    var nullableBoolean: Boolean? = null

    nullableInt = 10
    nullableDouble = 3.14
    nullableBoolean = true

    println("nullableInt: $nullableInt")
    println("nullableDouble: $nullableDouble")
    println("nullableBoolean: $nullableBoolean")
}
